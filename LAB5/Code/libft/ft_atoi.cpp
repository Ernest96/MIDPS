#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int nr;
	int n;

	i = 0;
	nr = 0;
	n = 0;
	while ((str[i] == '\t') || (str[i] == '\n') || (str[i] == ' ') ||
			(str[i] == '\f') || (str[i] == '\v') || (str[i] == '\r'))
		i++;
	if (str[i] == '-')
		n = 1;
	if ((str[i] == '+') || (str[i] == '-'))
		i++;
	while (str[i] && (str[i] >= '0') && (str[i] <= '9'))
	{
		nr *= 10;
		nr += (int)str[i] - '0';
		i++;
	}
	if (n == 1)
		return (-nr);
	else
		return (nr);
}
